ORG 0
START: MOV R0, #1
       SJMP CHECK

CHECK: CJNE R0, #1, NOT_ONE
       SJMP PROGRAM_ONE

NOT_ONE: CJNE R0, #2, NOT_TWO
         SJMP PROGRAM_TWO

NOT_TWO: CJNE R0, #4, NOT_FOUR
         SJMP PROGRAM_FOUR

NOT_FOUR: CJNE R0, #5, DEFAULT
          SJMP PROGRAM_FIVE

PROGRAM_ONE:
          SJMP DONE

PROGRAM_TWO:
          SJMP DONE

PROGRAM_FOUR:
          SJMP DONE

PROGRAM_FIVE:
          SJMP DONE

DEFAULT:
          SJMP DONE

DONE: SJMP DONE

END
